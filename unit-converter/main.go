package main // تعريف أن هذا الملف هو الملف الأساسي للبرنامج

import (
	"fmt" // استيراد حزمة التنسيق للطباعة والقراءة من الشاشة
)

func main() { // نقطة انطلاق تنفيذ البرنامج
	var choice int       // متغير لتخزين نوع التحويل الذي يختاره المستخدم
	var temperature float64 // متغير لتخزين قيمة درجة الحرارة المدخلة

	fmt.Println("--- محول درجات الحرارة ---") // طباعة عنوان البرنامج للمستخدم
	fmt.Println("1. من سلزيوس إلى فهرنهايت") // عرض الخيار الأول للتحويل
	fmt.Println("2. من فهرنهايت إلى سلزيوس") // عرض الخيار الثاني للتحويل
	fmt.Print("اختر نوع التحويل (1 أو 2): ") // طلب اختيار نوع التحويل من المستخدم
	fmt.Scanln(&choice)                 // قراءة الخيار وتخزينه في المتغير choice

	if choice == 1 { // التحقق إذا اختار المستخدم التحويل من سلزيوس لفهرنهايت
		fmt.Print("أدخل درجة الحرارة بالسلزيوس: ") // طلب القيمة المراد تحويلها
		fmt.Scanln(&temperature)             // استقبال القيمة وتخزينها في المتغير temperature
		result := (temperature * 9 / 5) + 32  // تطبيق المعادلة الرياضية للتحويل إلى فهرنهايت
		fmt.Printf("%.2f سلزيوس تساوي %.2f فهرنهايت\n", temperature, result) // طباعة النتيجة بتقريب لرقمين عشريين
	} else if choice == 2 { // التحقق إذا اختار المستخدم التحويل من فهرنهايت لسلزيوس
		fmt.Print("أدخل درجة الحرارة بالفهرنهايت: ") // طلب القيمة المراد تحويلها
		fmt.Scanln(&temperature)               // استقبال القيمة وتخزينها في المتغير temperature
		result := (temperature - 32) * 5 / 9    // تطبيق المعادلة الرياضية للتحويل إلى سلزيوس
		fmt.Printf("%.2f فهرنهايت تساوي %.2f سلزيوس\n", temperature, result) // طباعة النتيجة النهائية للمستخدم
	} else { // في حال أدخل المستخدم رقماً غير 1 أو 2
		fmt.Println("اختيار غير صحيح، يرجى إعادة المحاولة.") // تنبيه المستخدم بخطأ في الاختيار
	}
}
