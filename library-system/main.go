package main // تعريف أن هذا الملف هو نقطة انطلاق البرنامج

import (
	"fmt" // استيراد حزمة الطباعة والتنسيق
)

// تعريف هيكل (Struct) لتمثيل بيانات الكتاب
type Book struct {
	ID     int    // معرف فريد للكتاب
	Title  string // عنوان الكتاب
	Author string // اسم المؤلف
	IsAvailable bool // حالة الكتاب (متاح أم مستعار)
}

func main() { // الدالة الرئيسية للبرنامج
	// إنشاء قائمة (Slice) لتخزين مجموعة من الكتب بناءً على الهيكل Book
	var library []Book 

	// إضافة كتاب أول إلى القائمة يدوياً
	book1 := Book{ID: 1, Title: "لغة جو للمبتدئين", Author: "أحمد", IsAvailable: true}
	library = append(library, book1) // إلحاق الكتاب الأول بمصفوفة المكتبة

	// إضافة كتاب ثاني إلى القائمة
	book2 := Book{ID: 2, Title: "برمجة الأنظمة", Author: "سارة", IsAvailable: false}
	library = append(library, book2) // إلحاق الكتاب الثاني بمصفوفة المكتبة

	fmt.Println("--- نظام إدارة المكتبة ---") // طباعة عنوان البرنامج
	fmt.Printf("%-5s %-20s %-15s %-10s\n", "ID", "العنوان", "المؤلف", "الحالة") // طباعة رؤوس الجدول بتنسيق محاذى
	fmt.Println("------------------------------------------------------------") // خط فاصل للتنظيم

	// حلقة تمر على كل كتاب في مصفوفة المكتبة لعرض بياناته
	for _, b := range library {
		status := "متاح" // نص افتراضي للحالة
		if !b.IsAvailable { // التحقق إذا كان الكتاب غير متاح
			status = "مستعار" // تغيير النص إلى مستعار
		}
		// طباعة بيانات الكتاب بتنسيق أعمدة مرتبة
		fmt.Printf("%-5d %-20s %-15s %-10s\n", b.ID, b.Title, b.Author, status)
	}
}

