package main // تعريف أن هذا الملف هو نقطة انطلاق البرنامج

import (
	"fmt"       // استيراد حزمة الطباعة والقراءة
	"math/rand" // استيراد حزمة العمليات العشوائية
	"time"      // استيراد حزمة الوقت لضبط عشوائية المولد
)

func main() { // الدالة الرئيسية للبرنامج
	rand.Seed(time.Now().UnixNano()) // ضبط بذرة العشوائية بناءً على الوقت الحالي لضمان كلمة مرور مختلفة كل مرة

	var length int // تعريف متغير لتخزين طول كلمة المرور المطلوبة
	fmt.Print("أدخل طول كلمة المرور المطلوب: ") // طلب طول كلمة المرور من المستخدم
	fmt.Scanln(&length) // استقبال الطول وتخزينه في المتغير length

	// تعريف سلسلة نصية تحتوي على جميع الحروف والأرقام والرموز الممكن استخدامها
	chars := "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789!@#$%^&*()" 

	password := make([]byte, length) // إنشاء مصفوفة بايتات (Bytes) بالطول الذي حدده المستخدم لتخزين كلمة المرور

	for i := 0; i < length; i++ { // بدء حلقة تكرارية تدور بعدد مرات طول كلمة المرور
		randomIndex := rand.Intn(len(chars)) // توليد رقم عشوائي يمثل موقع (Index) حرف معين من سلسلة chars
		password[i] = chars[randomIndex]    // وضع الحرف المختار عشوائياً في المكان الحالي داخل مصفوفة كلمة المرور
	}

	finalPassword := string(password) // تحويل مصفوفة البايتات إلى نص (String) نهائي قابل للقراءة
	fmt.Printf("كلمة المرور المقترحة هي: %s\n", finalPassword) // طباعة كلمة المرور النهائية للمستخدم
}
