package main // تحديد أن هذا الملف هو نقطة انطلاق البرنامج

import (
	"fmt"      // حزمة للطباعة والقراءة من الشاشة
	"math/rand" // حزمة لتوليد الأرقام العشوائية
	"time"     // حزمة للتعامل مع الوقت لضبط عشوائية الأرقام
)

func main() { // الدالة الأساسية التي يبدأ منها الكود
	rand.Seed(time.Now().UnixNano()) // استخدام الوقت الحالي كـ "بذرة" لضمان توليد رقم مختلف في كل مرة
	secretNumber := rand.Intn(100) + 1 // توليد رقم عشوائي بين 1 و 100 وتخزينه في المتغير
	var guess int // تعريف متغير لتخزين تخمين المستخدم
	attempts := 0 // تعريف عداد لحساب عدد المحاولات التي قام بها اللاعب

	fmt.Println("مرحباً بك في لعبة تخمين الرقم!") // طباعة رسالة ترحيبية للاعب
	fmt.Println("لقد اخترت رقماً بين 1 و 100. هل يمكنك تخمينه؟") // شرح هدف اللعبة للمستخدم

	for { // بدء حلقة تكرارية (Loop) لا تنتهي حتى يتم كسرها بكلمة break
		attempts++ // زيادة عداد المحاولات بواحد في كل مرة يدخل فيها اللاعب تخميناً
		fmt.Print("أدخل تخمينك: ") // طلب إدخال الرقم من المستخدم
		fmt.Scanln(&guess) // قراءة الرقم المدخل وتخزينه في المتغير guess

		if guess < secretNumber { // التحقق إذا كان تخمين المستخدم أصغر من الرقم السري
			fmt.Println("خطأ! الرقم السري أكبر من ذلك.") // إعطاء تلميح للمستخدم بأن الرقم المطلوب أكبر
		} else if guess > secretNumber { // التحقق إذا كان تخمين المستخدم أكبر من الرقم السري
			fmt.Println("خطأ! الرقم السري أصغر من ذلك.") // إعطاء تلميح للمستخدم بأن الرقم المطلوب أصغر
		} else { // في حال كان التخمين مساوياً للرقم السري تماماً
			fmt.Printf("مبروك! لقد خمنت الرقم الصحيح وهو %d\n", secretNumber) // تهنئة اللاعب وإظهار الرقم
			fmt.Printf("لقد استغرقت %d محاولات.\n", attempts) // إظهار إجمالي عدد المحاولات التي استغرقها
			break // الخروج من الحلقة التكرارية وإنهاء البرنامج بنجاح
		}
	}
}
