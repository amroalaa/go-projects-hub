package main // تعريف أن هذا الملف هو نقطة انطلاق البرنامج

import (
	"fmt" // استيراد حزمة التنسيق للطباعة والقراءة
)

func main() { // الدالة الأساسية التي يبدأ منها الكود
	var tasks []string // تعريف مصفوفة ديناميكية (Slice) من النصوص لتخزين المهام
	var choice int     // متغير لتخزين رقم الخيار الذي يختاره المستخدم

	fmt.Println("--- مدير المهام البسيط ---") // طباعة عنوان البرنامج

	for { // بدء حلقة تكرارية لا تنتهي لعرض القائمة باستمرار حتى يقرر المستخدم الخروج
		fmt.Println("\n1. إضافة مهمة")   // خيار إضافة مهمة جديدة
		fmt.Println("2. عرض المهام")     // خيار عرض كل المهام الحالية
		fmt.Println("3. حذف جميع المهام") // خيار لتفريغ القائمة
		fmt.Println("4. خروج")          // خيار لإغلاق البرنامج
		fmt.Print("اختر إجراءً: ")      // طلب مدخل من المستخدم
		fmt.Scanln(&choice)             // قراءة رقم الخيار وتخزينه

		if choice == 1 { // التحقق إذا كان الخيار هو الإضافة
			var newTask string             // متغير مؤقت لتخزين نص المهمة الجديدة
			fmt.Print("اكتب المهمة: ")     // طلب نص المهمة من المستخدم
			fmt.Scanln(&newTask)           // استقبال النص وتخزينه
			tasks = append(tasks, newTask) // إضافة المهمة الجديدة إلى نهاية مصفوفة المهام
			fmt.Println("تمت إضافة المهمة بنجاح!") // تأكيد الإضافة للمستخدم
		} else if choice == 2 { // التحقق إذا كان الخيار هو العرض
			fmt.Println("--- قائمتك الحالية ---") // عنوان فرعي لعرض المهام
			if len(tasks) == 0 { // التأكد مما إذا كانت القائمة فارغة
				fmt.Println("القائمة فارغة حالياً.") // إبلاغ المستخدم بعدم وجود مهام
			} else { // في حال وجود مهام في المصفوفة
				for i, task := range tasks { // حلقة تمر على كل عنصر في المصفوفة (i هو الترتيب، task هو النص)
					fmt.Printf("%d. %s\n", i+1, task) // طباعة رقم المهمة ونصها بشكل مرتب
				}
			}
		} else if choice == 3 { // التحقق إذا كان الخيار هو الحذف الشامل
			tasks = nil // إفراغ المصفوفة تماماً عبر جعلها "لا شيء"
			fmt.Println("تم حذف جميع المهام.") // تأكيد عملية الحذف للمستخدم
		} else if choice == 4 { // التحقق إذا كان الخيار هو الخروج
			fmt.Println("وداعاً!") // طباعة رسالة وداع
			break // كسر الحلقة التكرارية لإنهاء البرنامج
		} else { // في حال أدخل المستخدم رقماً غير موجود في القائمة
			fmt.Println("خيار خاطئ، حاول مرة أخرى.") // تنبيه المستخدم بالخطأ
		}
	}
}
