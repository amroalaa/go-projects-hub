package main // تعريف أن هذا الملف هو نقطة انطلاق البرنامج

import (
	"fmt"      // استيراد حزمة الطباعة
	"net/http" // استيراد حزمة التعامل مع طلبات الويب لـفحص المواقع
	"sync"     // استيراد حزمة التزامن لانتظار انتهاء المهام المتعددة
)

// دالة لفحص موقع واحد وإخبارنا بحالته
func checkSite(site string, wg *sync.WaitGroup) { 
	defer wg.Done() // إبلاغ "مجموعة الانتظار" بأن هذه المهمة قد انتهت عند خروج الدالة

	res, err := http.Get(site) // محاولة الاتصال بالموقع وجلب بياناته
	if err != nil { // في حال فشل الاتصال بالموقع (موقع غير موجود أو إنترنت مقطوع)
		fmt.Printf("[-] الموقع %s: قد يكون متوقفاً عن العمل!\n", site) // طباعة رسالة فشل
		return // الخروج من الدالة الحالية
	} // ينتهي شرط الخطأ هنا

	fmt.Printf("[+] الموقع %s: يعمل بنجاح (الحالة: %d)\n", site, res.StatusCode) // طباعة حالة النجاح
} // تنتهي دالة الفحص هنا

func main() { // الدالة الرئيسية للبرنامج
	// قائمة ببعض المواقع التي نريد فحصها دفعة واحدة
	links := []string{
		"http://google.com",
		"http://facebook.com",
		"http://stackoverflow.com",
		"http://golang.org",
		"http://github.com",
	}

	var wg sync.WaitGroup // تعريف "مجموعة انتظار" لتتبع عدد المهام الجارية

	fmt.Println("جاري فحص حالة المواقع بالتوازي...") // طباعة رسالة بدء العمل

	for _, link := range links { // حلقة تمر على كل رابط في القائمة
		wg.Add(1) // إضافة مهمة جديدة إلى عداد مجموعة الانتظار قبل البدء
		go checkSite(link, &wg) // تشغيل دالة الفحص كـ "Goroutine" (مهمة خلفية مستقلة)
	} // ينتهي دوران الحلقة هنا (سيتم تشغيل جميع الفحوصات في نفس اللحظة تقريباً)

	wg.Wait() // أمر للبرنامج الرئيسي بالانتظار وعدم الإغلاق حتى تنتهي جميع المهام
	fmt.Println("تم الانتهاء من فحص جميع المواقع.") // رسالة ختامية بعد انتهاء المهام
}
