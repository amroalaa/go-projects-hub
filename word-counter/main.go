package main // تعريف أن هذا الملف هو نقطة انطلاق البرنامج

import (
	"fmt"    // استيراد حزمة الطباعة والقراءة
	"strings" // استيراد حزمة التعامل مع النصوص لتقطيع الجمل
)

func main() { // الدالة الرئيسية للبرنامج
	var text string // تعريف متغير نصي لتخزين الجملة المدخلة
	fmt.Println("--- محلل تكرار الكلمات ---") // طباعة عنوان البرنامج
	fmt.Println("أدخل جملة لتحليلها:") // طلب النص من المستخدم
	
	// ملاحظة: Scanln تتوقف عند أول مسافة، لذا سنستخدم طريقة بسيطة هنا للتجربة
	// في المشاريع المتقدمة نستخدم bufio لقراءة سطر كامل بمسافاته
	fmt.Scanln(&text) 

	// مثال توضيحي لنص ثابت إذا أردت تجربة تحليل نص طويل ومباشر:
	exampleText := "go is fast go is simple go is open source" // نص تجريبي للدراسة
	
	words := strings.Fields(exampleText) // تقطيع النص إلى مصفوفة من الكلمات بناءً على المسافات
	
	wordCounts := make(map[string]int) // إنشاء خريطة (Map) مفتاحها نص (الكلمة) وقيمتها رقم (عدد التكرار)

	for _, word := range words { // حلقة تمر على كل كلمة في المصفوفة الناتجة عن التقطيع
		wordCounts[word]++ // زيادة عداد هذه الكلمة بمقدار 1 داخل الخريطة
	}

	fmt.Println("\nنتائج التحليل:") // طباعة عنوان النتائج
	for word, count := range wordCounts { // حلقة تمر على الخريطة لعرض كل كلمة وتكرارها
		fmt.Printf("الكلمة '%s': ظهرت %d مرات\n", word, count) // طباعة الكلمة وعدد تكرارها الملحق بها
	}
}
