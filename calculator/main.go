package main // تعريف أن هذا الملف هو نقطة البداية للبرنامج

import (
	"fmt" // استيراد حزمة التنسيق للطباعة والقراءة من الشاشة
)

func main() { // الدالة الرئيسية التي يبدأ منها تنفيذ البرنامج
	var num1, num2 float64 // تعريف متغيرين من نوع الأرقام العشرية لتخزين المدخلات
	var operator string    // تعريف متغير نصي لتخزين العملية الحسابية (+، -، *، /)

	fmt.Print("أدخل الرقم الأول: ") // طباعة نص يطلب من المستخدم إدخال الرقم الأول
	fmt.Scanln(&num1)         // استقبال الرقم الأول وتخزينه في مكان المتغير num1 بالذاكرة

	fmt.Print("أدخل العملية (+, -, *, /): ") // طلب اختيار العملية الحسابية من المستخدم
	fmt.Scanln(&operator)                  // استقبال الرمز وتخزينه في المتغير operator

	fmt.Print("أدخل الرقم الثاني: ") // طباعة نص يطلب من المستخدم إدخال الرقم الثاني
	fmt.Scanln(&num2)          // استقبال الرقم الثاني وتخزينه في مكان المتغير num2 بالذاكرة

	switch operator { // استخدام قاعدة switch للمقارنة بين أنواع العمليات المختارة
	case "+": // في حال كانت العملية هي الجمع
		fmt.Printf("النتيجة: %v\n", num1+num2) // طباعة مجموع الرقمين
	case "-": // في حال كانت العملية هي الطرح
		fmt.Printf("النتيجة: %v\n", num1-num2) // طباعة ناتج طرح الرقمين
	case "*": // في حال كانت العملية هي الضرب
		fmt.Printf("النتيجة: %v\n", num1*num2) // طباعة ناتج ضرب الرقمين
	case "/": // في حال كانت العملية هي القسمة
		if num2 != 0 { // التأكد من أن الرقم الثاني ليس صفراً لتجنب خطأ برمجبي
			fmt.Printf("النتيجة: %v\n", num1/num2) // طباعة ناتج القسمة في حال كان الرقم صالحاً
		} else { // في حال كان الرقم الثاني صفراً
			fmt.Println("خطأ: لا يمكن القسمة على صفر!") // طباعة رسالة تنبيه للمستخدم
		}
	default: // في حال أدخل المستخدم رمزاً غير معروف
		fmt.Println("عملية غير صالحة!") // طباعة رسالة تفيد بأن الرمز المختار خاطئ
	}
}
